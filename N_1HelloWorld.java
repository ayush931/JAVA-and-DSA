// Some Rules in Java

/*
 * Rule 1:- Variable name should not begin with a number.
 * Rule 2:- Whitespace is not permitted in variable names.
 * "moneyBorrowe d" is not allowed.
 * Rule 3:- A Java keyword (reserve word) cannot be used as a variable name.
 * Rule 4:- It is preferred to use variable names with more than one word all lowercase letters for the first word and captialization of the first letter of each subsequent word.
 * Rule 5:- When creating variables, it is preferred to give them meaningful names.
 * Rule 6:- All lowercase letters should be used when creating one-word variable names.
 */

