public class N2_Datatypes {
    public static void main(String[] args) {
        int money = 2000; // 'int' is datatype called integer.
        System.out.println(money);
        money = 5000;
        System.out.println(money);

        // Codes of Datatypes;

        boolean flag = true;
        int a = 12345;
        short s = 1200;
        byte b = 120;
        long l = 123456789;
        float f = 800.234f;
        double d = 7412.0325;
        char ch = 'p';

        System.out.println("The boolean variable is " + flag);
        System.out.println("The integer variable is " + a);
        System.out.println("The Short variable is " + s);
        System.out.println("The byte variable is " + b);
        System.out.println("The long variable is " + l);
        System.out.println("The float variable is " + f);
        System.out.println("The double variable is " + d);
        System.out.println("The char variable is " + ch);
    }
}

// Primitive Data Types

/*
 * 1. Built in Java
 * 2. Hold single values
 * 3. Cannot be divided
 */

// Examples are 

/*
 * 1. boolean (true or false) :- boolean flag = false;
 * 2. char {(Characters), Size: 2 bytes or 16 bits} :- char ch = 'p';
 * 3. byte {(integer), Size: 1 byte or 8 bits, Range:- -128 to 127} :- byte b = 60;
 * 4. short {(integer), Size: 2 bytes / 16 bits, Range:- -32,768 to 32,768} :- short s = 1000;
 * 5. int  {(integer), Size: 4 bytes or 32 bits, Range:- -2^31 to to 2^31 - 1} :- int a = 1000;
 * 6. long {(integer), Size: 8 bytes or 64 bits, Range:- -2^64 to 2^64 - 1} :- long l  = 12345678;
 * 7. float {(Fractional), Size: 4 bytes or 32 bits} :- float f = 123.456f;
 * 8. double {(Fraction), Size: 8 bytes or 64 bits} :- double d= 4213.236;
 */

