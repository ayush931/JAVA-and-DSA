public class N9_RelationalOperators {
    public static void main(String[] args) {
        int p = 10, q = 15;

        System.out.println(p == q); //False
        System.out.println(p != q); //true
        System.out.println(p <= q); //true
        System.out.println(p >= q); //false
        System.out.println(p < q); //true
        System.out.println(p > q); //false
    }
}

// Java relational operators

/*
* == is equal to
* != not equal to
* > greater than
* < less than
* > == greater than or equal to
* < == lesser than or equal to
*/